    VanillaItems(final Material material, final String filename, final int ascent, final int height, final int rows, final char character) {
        this.material = material;
        this.emojiName = name().toLowerCase();
        this.filename = filename;
        this.ascent = ascent;
        this.height = height;
        this.rows = rows;
        this.character = character;
        final TextColor color;
        switch (material) {
        case SHORT_GRASS:
        case OAK_LEAVES:
        case SPRUCE_LEAVES:
        case BIRCH_LEAVES:
        case ACACIA_LEAVES:
        case DARK_OAK_LEAVES:
        case JUNGLE_LEAVES:
        case MANGROVE_LEAVES:
        case VINE:
        case LILY_PAD:
        case FERN:
        case TALL_GRASS:
        case LARGE_FERN:
        case MELON_STEM:
        case PUMPKIN_STEM:
            color = TextColor.color(0x00FF00);
            break;
        default:
            color = TextColor.color(0xFFFFFF);
            break;
        }
        this.component = Component.text(character)
            .style(Style.style()
                   .font(Key.key("cavetale:default"))
                   .color(color));
    }

