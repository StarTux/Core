    public static VanillaItems of(Material material) {
        return MATERIAL_MAP.get(material);
    }

    public static VanillaItems of(String key) {
        try {
            return VanillaItems.valueOf(key.toUpperCase());
        } catch (IllegalArgumentException iae) {
            return null;
        }
    }

    public static Component componentOf(Material material) {
        VanillaItems glyph = VanillaItems.of(material);
        return glyph != null ? glyph.component : Component.empty();
    }

    @Override
    public Component asComponent() {
        return component;
    }

    @Override
    public Component getDisplayName() {
        return material.isItem()
            ? translatable(new ItemStack(material))
            : text(toCamelCase(" ", this));
    }

    @Override
    public int getPixelWidth() {
        return 16;
    }

    @Override
    public boolean isEssential() {
        switch (name()) {
        case "LADDER":
        case "IRON_SWORD":
        case "GOLDEN_SWORD":
        case "DIAMOND_SWORD":
        case "NETHERITE_SWORD":
        case "ARROW":
        case "SPECTRAL_ARROW":
        case "SHORT_GRASS":
        case "GRASS_BLOCK":
        case "AXOLOTL_BUCKET":
        case "SNOWBALL":
        case "POWDER_SNOW_BUCKET":
        case "LAVA_BUCKET":
        case "TROPICAL_FISH_BUCKET":
        case "WATER_BUCKET":
        case "IRON_SHOVEL":
        case "CARROT":
        case "GOLDEN_SHOVEL":
        case "VINE":
            return true;
        default: return false;
        }
    }

    public static void test() {
        // Find missing VanillaItems (informal)
        for (Material material : Material.values()) {
            if (material.isLegacy()) continue;
            if (!material.isItem()) continue;
            if (material.isAir()) continue;
            VanillaItems vanillaItems = VanillaItems.of(material);
            if (vanillaItems != null) continue;
            if (material.name().endsWith("_SPAWN_EGG")) continue;
            plugin().getLogger().warning("No VanillaItems: " + material);
        }
    }
